package ua.itea.javabasic.practice.lesson15.credential;
public class FirstCredential {

    private char[] login;
    private Byte pin;

    public char[] getLogin() {
        return login;
    }

    public void setLogin(char[] login) {
        this.login = login;
    }

    public Byte getPin() {
        return pin;
    }

    public void setPin(Byte pin) {
        this.pin = pin;
    }
}
